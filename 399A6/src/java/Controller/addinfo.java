/**
 *
 * @author Victor
 */
public class addinfo extends HttpServlet {

    @PersistenceUnit(unitName="399A6PU")
    private EntityManagerFactory emf;
    @Resource
    private UserTransaction utx;
    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        RequestDispatcher view = request.getRequestDispatcher("addinfo.jsp");
        view.forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        try {
            String showid = request.getParameter("si");
            
            String[] pics;
            pics = request.getParameterValues("pics[]");
            
            String[] comms;
            comms = request.getParameterValues("comms[]");
            
            
            utx.begin();
            EntityManager em = emf.createEntityManager();
            for(int i = 0;i < pics.length;i++){
                Ecpresentations etemp = em.getReference(Ecpresentations.class, showid);
                Ecinfo ef = new Ecinfo();
                ef.setPicy(pics[i]);
                ef.setDetails(comms[i]);
                ef.setShowid(etemp);
                em.persist(ef);             
            }
            utx.commit();
            RequestDispatcher view = request.getRequestDispatcher("addinforeport.jsp");
            view.forward(request, response);
            em.close();
        } catch (NotSupportedException ex) {
            ex.printStackTrace();
        } catch (SystemException ex) {
            ex.printStackTrace();
        } catch (RollbackException ex) {
            ex.printStackTrace();
        } catch (HeuristicMixedException ex) {
            ex.printStackTrace();
        } catch (HeuristicRollbackException ex) {
            ex.printStackTrace();
        } catch (SecurityException ex) {
            ex.printStackTrace();
        } catch (IllegalStateException ex) {
            ex.printStackTrace();
        }
    }

    /**
     * Returns a short description of the servlet.
     *
     * @return a String containing servlet description
     */
    @Override
    public String getServletInfo() {
        return "Short description";
    }// </editor-fold>

}
